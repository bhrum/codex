use fabushi_telegram_protocol::{
    audit_mtproto_api_schema, audit_telegram_api_schema, parse_schema_catalog, DeclarationKind,
    SchemaCatalog,
};
use std::{env, fs, path::Path, process};

fn main() {
    let arguments: Vec<_> = env::args().skip(1).collect();
    let [telegram_path, mtproto_path, output_path] = arguments.as_slice() else {
        eprintln!("usage: tl-schema-codegen <telegram_api.tl> <mtproto_api.tl> <output.rs>");
        process::exit(2);
    };
    let telegram_bytes = read(telegram_path);
    let mtproto_bytes = read(mtproto_path);
    audit_telegram_api_schema(&telegram_bytes).unwrap_or_else(|error| {
        eprintln!("telegram_api.tl audit failed: {error}");
        process::exit(1);
    });
    audit_mtproto_api_schema(&mtproto_bytes).unwrap_or_else(|error| {
        eprintln!("mtproto_api.tl audit failed: {error}");
        process::exit(1);
    });
    let telegram = parse_schema_catalog(&String::from_utf8_lossy(&telegram_bytes))
        .expect("audited telegram catalog");
    let mtproto = parse_schema_catalog(&String::from_utf8_lossy(&mtproto_bytes))
        .expect("audited MTProto catalog");
    let generated = generate(&telegram, &mtproto);
    if let Some(parent) = Path::new(output_path).parent() {
        fs::create_dir_all(parent).unwrap_or_else(|error| {
            eprintln!("failed to create {}: {error}", parent.display());
            process::exit(1);
        });
    }
    fs::write(output_path, generated).unwrap_or_else(|error| {
        eprintln!("failed to write {output_path}: {error}");
        process::exit(1);
    });
}

fn read(path: &str) -> Vec<u8> {
    fs::read(path).unwrap_or_else(|error| {
        eprintln!("failed to read {path}: {error}");
        process::exit(2);
    })
}

fn generate(telegram: &SchemaCatalog, mtproto: &SchemaCatalog) -> String {
    let mut output = String::from(
        "// @generated by tl-schema-codegen. Do not edit manually.\n\
         // Source commit: a17f87c4cff7b90b278d12b91ba0614383aaee82\n\n\
         #[rustfmt::skip]\n\
         pub const WIRE_CONSTRUCTORS: &[WireConstructor] = &[\n",
    );
    append_catalog(&mut output, "TelegramApi", telegram);
    append_catalog(&mut output, "MtprotoApi", mtproto);
    output.push_str("];\n");
    output
}

fn append_catalog(output: &mut String, schema: &str, catalog: &SchemaCatalog) {
    for declaration in catalog.types.iter().chain(&catalog.functions) {
        let Some(id) = declaration.constructor_id else {
            continue;
        };
        let kind = match declaration.kind {
            DeclarationKind::Type => "Type",
            DeclarationKind::Function => "Function",
        };
        let name = serde_json::to_string(&declaration.name).expect("JSON string");
        let result_type = serde_json::to_string(&declaration.result_type).expect("JSON string");
        output.push_str(&format!(
            "    WireConstructor {{ schema: WireSchema::{schema}, name: {name}, id: 0x{id:08x}, kind: DeclarationKind::{kind}, result_type: {result_type} }},\n"
        ));
    }
}
